API_DISCLAIMER: Dict[str, Any] = {
    "disclaimer": "AI-generated review requires human oversight",
    "human_review_required": True,
    "intended_use": "preliminary_assessment_only",
    "warning": "Do not use for final publication decisions without human expert validation",
}


class DisclaimerService:
    """Service providing consistent disclaimer text for different outputs."""

    @staticmethod
    def get_system_disclaimer() -> str:
        """Return the full system disclaimer used in interactive UI or logs."""
        return SYSTEM_DISCLAIMER

    @staticmethod
    def get_pdf_disclaimer() -> str:
        """Return a short disclaimer suitable for inclusion in generated PDFs."""
        return PDF_DISCLAIMER

    @staticmethod
    def get_api_disclaimer() -> Dict[str, Any]:
        """Return a structured disclaimer suitable for API responses."""
        return API_DISCLAIMER.copy()


